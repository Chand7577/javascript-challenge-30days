export const PI = 3.14159;
export const GRAVITY = 9.81;

// Functions
export function square(x) {
  return x ** 2;
}

export function cube(x) {
  return x ** 3;
}

export function areaOfCircle(radius) {
  return PI * radius ** 2;
}

export function areaOfRectangle(length, width) {
  return length * width;
}